#include "libft.h"

int		ft_memdel2dim(void ***t, size_t n)
{
	while (n--)
	{
		if ((*t)[n])
		{
			free((*t)[n]);
			(*t)[n] = NULL;
		}
	}
	if (*t)
	{
		free(*t);
		*t = NULL;
	}
	return (0);
}
