#include "libft.h"

int		ft_atoi(const char *str)
{
	size_t	i;
	int		sign;
	long	n;

	i = 0;
	n = 0;
	sign = 1;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
			|| str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] && '0' <= str[i] && str[i] <= '9')
	{
		n = (10 * n) + str[i] - '0';
		i++;
	}
	return ((int)(sign * n));
}
